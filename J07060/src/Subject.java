public class Subject {
    private String idSub, nameSub, form;
    
    public Subject(String idSub, String nameSub, String form){
        this.idSub = idSub;
        this.nameSub = nameSub;
        this.form = form;
    }

    public String getIdSub() {
        return idSub;
    }

    public String getNameSub() {
        return nameSub;
    }

    public String getForm() {
        return form;
    }
    
}
